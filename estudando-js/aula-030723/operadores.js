const num1 = 5;
const num2 = 8;
const num3 = 4.5;

/*
  Operadores Artiméticos
  + Adição
  - Subtração
  * Multiplicação
  / Divisão
  ** Exponenciação
  % Resto da divisão
*/


let result;

result = num1 + num2;
console.log(result);

result = num1 - num2;
console.log(result);

result = num1 * num2;
console.log(result);

result = num1 / num2;
console.log(result);

result = num1 ** num2;
console.log(result);

result = num2 % num3;
console.log(result);