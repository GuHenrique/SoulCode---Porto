CREATE DATABASE spotify_db;

USE spotify_db;

CREATE TABLE usuario(
	idUsuario INTEGER PRIMARY KEY AUTO_INCREMENT,
    nome VARCHAR(200) NOT NULL,
    email VARCHAR(100) NOT NULL UNIQUE,
    senha VARCHAR(100) NOT NULL,
    dataNascimento DATE NOT NULL
);

CREATE TABLE playlist(
	idPlaylist INTEGER PRIMARY KEY AUTO_INCREMENT,
    descricao VARCHAR(300),
    titulo VARCHAR(50) NOT NULL,
    dataCriacao DATE,
    fkUsuario INTEGER NOT NULL,
    FOREIGN KEY (fkUsuario) REFERENCES usuario (idUsuario)
);

CREATE TABLE artista(
	idArtista INTEGER PRIMARY KEY AUTO_INCREMENT,
	nome VARCHAR(200) NOT NULL,
    nacionalidade VARCHAR(50) NOT NULL
);

CREATE TABLE album(
	idAlbum INTEGER PRIMARY KEY AUTO_INCREMENT,
    titulo VARCHAR(50) NOT NULL,
    dataLancamento DATE NOT NULL,
    duracaoTotal INTEGER NOT NULL,
    fkArtista INTEGER NOT NULL,
    FOREIGN KEY (fkArtista) REFERENCES artista (idArtista)
);

CREATE TABLE genero (
	idGenero INTEGER PRIMARY KEY AUTO_INCREMENT,
    nome VARCHAR(50) NOT NULL
);

CREATE TABLE musica (
	idMusica INTEGER PRIMARY KEY AUTO_INCREMENT,
    titulo VARCHAR(50) NOT NULL,
    duracao INTEGER NOT NULL,
	fkGenero INTEGER NOT NULL, 
	fkArtista INTEGER NOT NULL, 
	fkAlbum INTEGER NOT NULL, 
	FOREIGN KEY (fkGenero) REFERENCES genero(idGenero),
	FOREIGN KEY (fkArtista) REFERENCES artista(idArtista),
	FOREIGN KEY (fkAlbum) REFERENCES album(idAlbum)
);

CREATE TABLE usuario_musica (
	idFavorita INTEGER PRIMARY KEY AUTO_INCREMENT,
    fkUsuario INTEGER NOT NULL,
    fkMusica INTEGER NOT NULL,
    FOREIGN KEY (fkUsuario) REFERENCES usuario (idUsuario),
    FOREIGN KEY (fkMusica) REFERENCES musica (idMusica)
);



CREATE TABLE playlist_musica (
	idAdiciona INTEGER PRIMARY KEY AUTO_INCREMENT,
    fkPlaylist INTEGER NOT NULL,
    fkMusica INTEGER NOT NULL,
	FOREIGN KEY (fkPlaylist) REFERENCES playlist(idPlaylist),
	FOREIGN KEY (fkMusica) REFERENCES musica(idMusica)
);


